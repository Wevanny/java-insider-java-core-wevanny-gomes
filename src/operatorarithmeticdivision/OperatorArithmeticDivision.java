package operatorarithmeticdivision;
@SuppressWarnings("all")
public class OperatorArithmeticDivision {
    public static void main(String[] args) {
        int a = 4;
        int b = 2;
        int c = a / b;
        int d = b / a;
        double e = (double) b / a;
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
