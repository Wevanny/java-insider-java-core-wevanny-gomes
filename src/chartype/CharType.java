package chartype;

public class CharType {

    public static void main(String[] args) {
        char c1 = 'A';
        System.out.println(c1);

        //ASCII Table
        int i1 = c1;
        System.out.println(i1);

        char c2 = 'A' + 2;
        System.out.println(c2);

        char c3 = 48;
        System.out.println(c3);

        char c4 = 49;
        System.out.println(c4);

        char c5 = (char) -10;
        System.out.println(c5);
    }
}
